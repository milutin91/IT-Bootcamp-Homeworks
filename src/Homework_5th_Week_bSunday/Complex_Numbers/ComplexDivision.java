package Homework_5th_Week_bSunday.Complex_Numbers;

public class ComplexDivision extends Complex {

    public ComplexDivision(double a, double b) {
        super(a, b);
    }

    @Override
    public Complex divide(Complex t) {
        return super.divide(t);
    }

    @Override
    public String toString() {
        return super.toString();
    }
}
