package Homework_5th_Week_bSunday.Complex_Numbers;

public class ComplexSubtract extends Complex {

    public ComplexSubtract(double a, double b) {
        super(a, b);
    }

    @Override
    public Complex subtract(Complex t) {
        return super.subtract(t);
    }

    @Override
    public String toString() {
        return super.toString();
    }
}
