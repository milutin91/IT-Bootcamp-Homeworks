package Homework_5th_Week_bSunday.Complex_Numbers;

public class ComplexMultiply extends Complex {

    public ComplexMultiply(double a, double b) {
        super(a, b);
    }

    @Override
    public Complex multiply(Complex t) {
        return super.multiply(t);
    }

    @Override
    public String toString() {
        return super.toString();
    }
}
