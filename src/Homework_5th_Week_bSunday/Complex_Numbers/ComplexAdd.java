package Homework_5th_Week_bSunday.Complex_Numbers;

public class ComplexAdd extends Complex {

    public ComplexAdd(double a, double b) {
        super(a, b);
    }

    @Override
    public Complex add(Complex t) {
        return super.add(t);
    }

    @Override
    public String toString() {
        return super.toString();
    }
}
